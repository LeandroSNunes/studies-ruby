# Introdução
- Criada por Yukihiro Matsumoto, mais conhecido como Matz, no ano de 1995 no Japão, com o objetivo de
ser uma linguagem mais legível e agradável de se programar
- Inspirada em outras linguagens como Perl, Smalltalk e Lisp
- Ruby é uma linguagem interpretada, existe um arquivo com a extensão .rb e um programa cujo papel é
interpretar o conteúdo deste arquivo, transformando-o em instruções de máquina e executando o
comportamento esperado

